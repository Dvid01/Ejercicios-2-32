public class Ejercicio4 {
    
    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        int suma = x+y;
        int rest = x-y;
        int mult = x*y;
        int divi = x/y;
        
        System.out.println(x + "+" + y + " = " + suma);
        System.out.println(x + "-" + y + " = " + rest);
        System.out.println(x + "*" + y + " = " + mult);
        System.out.println(x + "/" + y + " = " + divi);
    }

}
